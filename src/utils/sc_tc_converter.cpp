std::string ScTcConverter::toTraditional(const std::string &utf8Simplified) const {
    std::string result;
    const unsigned char *p = reinterpret_cast<const unsigned char *>(utf8Simplified.data());
    const unsigned char *end = p + utf8Simplified.size();
    while (p < end) {
        char32_t cp = nextUtf8Codepoint(p, end);
        if (cp == 0) break;
        auto it = s2t_map.find(cp);
        appendUtf8(result, (it != s2t_map.end()) ? it->second : cp);
    }
    return result;
}

std::string ScTcConverter::toSimplified(const std::string &utf8Traditional) const {
    std::string result;
    const unsigned char *p = reinterpret_cast<const unsigned char *>(utf8Traditional.data());
    const unsigned char *end = p + utf8Traditional.size();
    while (p < end) {
        char32_t cp = nextUtf8Codepoint(p, end);
        if (cp == 0) break;
        auto it = t2s_map.find(cp);
        appendUtf8(result, (it != t2s_map.end()) ? it->second : cp);
    }
    return result;
}

