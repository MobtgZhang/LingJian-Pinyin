// 繁体→简体（反向映射）
static const std::unordered_map<char32_t, char32_t> t2s_map = []() {
    std::unordered_map<char32_t, char32_t> m;
    for (const auto &p : s2t_map) {
        m[p.second] = p.first;
    }
    return m;
}();

static char32_t nextUtf8Codepoint(const unsigned char *&p, const unsigned char *end) {
    if (p >= end) return 0;
    unsigned char c = *p++;
    if (c < 0x80) return c;
    if (c < 0xE0) {
        if (p >= end) return 0;
        char32_t cp = ((c & 0x1F) << 6) | (*p++ & 0x3F);
        return cp;
    }
    if (c < 0xF0) {
        if (p + 1 >= end) return 0;
        char32_t cp = ((c & 0x0F) << 12) | ((*p++ & 0x3F) << 6) | (*p++ & 0x3F);
        return cp;
    }
    if (p + 2 >= end) return 0;
    return ((c & 0x07) << 18) | ((*p++ & 0x3F) << 12) | ((*p++ & 0x3F) << 6) | (*p++ & 0x3F);
}

static void appendUtf8(std::string &out, char32_t cp) {
    if (cp < 0x80) {
        out += static_cast<char>(cp);
    } else if (cp < 0x800) {
        out += static_cast<char>(0xC0 | (cp >> 6));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else if (cp < 0x10000) {
        out += static_cast<char>(0xE0 | (cp >> 12));
        out += static_cast<char>(0x80 | ((cp >> 6) & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    } else {
        out += static_cast<char>(0xF0 | (cp >> 18));
        out += static_cast<char>(0x80 | ((cp >> 12) & 0x3F));
        out += static_cast<char>(0x80 | ((cp >> 6) & 0x3F));
        out += static_cast<char>(0x80 | (cp & 0x3F));
    }
}

